const looksAutoGenerated = (name) =>
  /^[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}$/i.test(name) ||
  /^\d{8,}_status_/.test(name) ||
  /^IMG-\d{8}-WA\d+$/i.test(name) ||
  /^FB_IMG_\d+$/i.test(name) ||
  /^WhatsApp Image/i.test(name) ||
  /^\d{4}-\d{2}-\d{2}$/.test(name);

const toPhotos = (raw, fallbackPrefix = 'Photo') => {
  const items = Object.entries(raw).map(([path, url]) => {
    const filename = path.split('/').pop().replace(/\.[^.]+$/, '');
    const cleaned  = filename.replace(/^\d+\.\s*/, '').trim();
    return { url, filename, caption: looksAutoGenerated(cleaned) ? '' : cleaned };
  });
  items.sort((a, b) =>
    a.filename.localeCompare(b.filename, undefined, { numeric: true })
  );
  let n = 1;
  return items.map((item) => ({
    ...item,
    caption: item.caption || `${fallbackPrefix} ${n++}`,
  }));
};

const galleryPhotos       = toPhotos(galleryRaw,  'Photo');
