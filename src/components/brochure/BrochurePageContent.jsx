/** Section card with crimson header bar */
function BrochureSection({ id, label, number, children }) {
  return (
    <section className="brochure-section" id={id}>
      <div className="bs-header">
        <span className="bs-number">{number}</span>
        <h2 className="bs-title">{label}</h2>
      </div>
      <div className="bs-body">{children}</div>
    </section>
  );
}

/** Responsive photo grid — shows full photos without truncation */
function PhotoGrid({ photos }) {
  if (!photos.length) {
    return <p className="photo-empty">Photos coming soon.</p>;
  }
  return (
    <div className={`photo-grid ${photos.length > 14 ? 'photo-grid--dense' : ''}`}>
      {photos.map((p, i) => (
        <div key={`${p.url}-${i}`} className="photo-item">
          <img src={p.url} alt={p.caption} loading="lazy" />
          <p className="photo-caption">{p.caption}</p>
        </div>
      ))}
    </div>
  );
}

/** Tribute text with optional scripture block and sign-off */
function TributeText({ tribute }) {
  const paras = tribute.body.trim().split(/\n{2,}/).map((s) => s.trim()).filter(Boolean);
  return (
    <div className="tribute-text">
      {tribute.subtitle && tribute.scripture && (
        <blockquote className="scripture-block">
          <cite className="scripture-ref">{tribute.subtitle}</cite>
          <p className="scripture-body">{tribute.scripture}</p>
        </blockquote>
      )}
      <div className="tribute-body">
        {paras.map((para, i) => (
          <p key={i} className={i === 0 ? 'tribute-first-para' : ''}>
            {para}
          </p>
        ))}
      </div>
      {tribute.signoff && (
        <div className="tribute-signoff">
          <p className="tribute-farewell">— {tribute.signoff}</p>
          {tribute.signatory && (
            <p className="tribute-signatory">{tribute.signatory}</p>
          )}
        </div>
      )}
    </div>
  );
}

